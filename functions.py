def force(m, a):
    return m * a

def kinetic_energy(m, v):
    return 0.5 * m * v * v

def ohm_current(v, r):
    return v / r

def work(f, d):
    return f * d

def power(w, t):
    return w / t

def pressure(f, a):
    return f / a

def density(m, v):
    return m / v

def momentum(m, v):
    return m * v
